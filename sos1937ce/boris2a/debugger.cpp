#include "BS2all.h"

#include <stdio.h>

debugger::debugger()
{
	status = true;	
}

bool debugger::checkstatus()
{
	bool ostatus = status;
	status = false;
	return(ostatus);
}

void debugger::Dprintf( char * s )
{
	if( checkstatus() )
	{
		fp = fopen( "DBG.XXX" , "wb" );
	}

	fprintf( fp,"%s\n\r", s);
	fflush(fp);
}

