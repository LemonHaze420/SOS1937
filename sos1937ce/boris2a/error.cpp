#include "BS2all.h"

#include <stdarg.h>

void __stdcall fatalError( char * errStr,... )
{
	char buf[256];
	va_list argptr;

	va_start(argptr,errStr);

	vsprintf(buf,errStr,argptr);
	va_end(argptr);

	OutputDebugString("\n\nFATAL ERROR: ");
	OutputDebugString(buf);
	OutputDebugString("\n\n");

	exit(0);
}


